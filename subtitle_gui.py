# Tag written to first line of every SRT we generate
OUR_TAG = "; generated by whisperx-subgen"

# ─── Library database ─────────────────────────────────────────────────────────
class LibraryDB:
    """Persistent JSON store tracking subtitle status per video file."""

    def __init__(self, path: Path):
        self.path = path
        self.data = {}
        self._load()

    def _load(self):
        if self.path.exists():
            try:
                import json
                self.data = json.loads(self.path.read_text(encoding="utf-8"))
            except Exception:
                self.data = {}

    def save(self):
        import json
        self.path.write_text(json.dumps(self.data, indent=2), encoding="utf-8")

    def update(self, video: Path, status: str, our_srt: bool, srt_path: str = "",
               fingerprint: str = ""):
        import time
        self.data[str(video)] = {
            "status":      status,
            "our_srt":     our_srt,
            "srt_path":    srt_path,
            "fingerprint": fingerprint,
            "scanned_at":  time.time(),
        }

    def get(self, video: Path) -> dict:
        return self.data.get(str(video), {})


def file_fingerprint(p: Path) -> str:
    """Fast fingerprint using size + mtime — no file reading needed."""
    try:
        st = p.stat()
        return f"{st.st_size}:{st.st_mtime:.0f}"
    except Exception:
        return ""


def is_our_srt(srt_path: Path) -> bool:
    """Check if an SRT file was generated by this app."""
    try:
        for enc in ("utf-8-sig", "utf-8", "latin-1"):
            try:
                first = srt_path.read_text(encoding=enc, errors="ignore")[:200]
                if OUR_TAG in first:
                    return True
                break
            except Exception:
                continue
        return False
    except Exception:
        return False


def scan_video_status(video: Path, lang: str,
                      cached: dict = None) -> tuple:
    """
    Returns (status, our_srt, srt_path, fingerprint).

    Checks all known SRT variants explicitly — does not rely solely on
    iterdir so it works reliably on NAS/network drives.
    """
    fp = file_fingerprint(video)
    stem = video.stem
    base = str(video.with_suffix(""))  # strip video extension

    # Explicitly check all common language variants — catches .en.srt, .nb.srt etc.
    COMMON_LANGS = ["en", "no", "nb", "sv", "da", "de", "fr", "es", "fi", "nl", "it"]
    explicit = [Path(f"{base}.{l}.srt") for l in COMMON_LANGS]
    explicit += [Path(f"{base}.srt")]
    if lang not in COMMON_LANGS:
        explicit.insert(0, Path(f"{base}.{lang}.srt"))

    # Also scan directory for any other variants we may have missed
    try:
        dir_srts = [f for f in video.parent.iterdir()
                    if f.suffix.lower() == ".srt"
                    and f.stem.lower().startswith(stem.lower())]
    except Exception:
        dir_srts = []

    # Deduplicate, explicit first
    seen = set()
    candidates = []
    for s in explicit + dir_srts:
        if s not in seen:
            seen.add(s)
            candidates.append(s)

    for srt in candidates:
        if srt.exists():
            ours = is_our_srt(srt)
            if ours:
                if cached and cached.get("fingerprint") and cached["fingerprint"] != fp:
                    return "CHANGED", True, str(srt), fp
                return "DONE", True, str(srt), fp
            else:
                return "EXTERNAL", False, str(srt), fp

    # No subtitle found — use cache only for NEW vs UNPROCESSED distinction
    if cached and cached.get("scanned_at"):
        return "UNPROCESSED", False, "", fp
    return "NEW", False, "", fp


# ─── Styled button ────────────────────────────────────────────────────────────
class FlatButton(tk.Button):
    def __init__(self, parent, text, command=None, accent=False, width=None, height=None, **kw):
        bg = ACCENT if accent else BG_INPUT
        fg = "#000000" if accent else TEXT
        abg = ACCENT_DIM if accent else BG_HOVER
        super().__init__(
            parent, text=text, command=command,
            bg=bg, fg=fg, activebackground=abg, activeforeground=fg,
            relief="flat", borderwidth=0,
            font=("Consolas", 10, "bold"),
            cursor="hand2", padx=10, pady=4,
            **kw
        )
        self._accent = accent
        self._bg = bg
        self._abg = abg
        self.bind("<Enter>", lambda e: self.configure(bg=abg))
        self.bind("<Leave>", lambda e: self.configure(bg=bg))

    def configure_text(self, text):
        self.configure(text=text)


# ─── Section label ────────────────────────────────────────────────────────────
def section_label(parent, text):
    f = tk.Frame(parent, bg=BG_PANEL)
    tk.Label(f, text=text.upper(), font=("Consolas", 9, "bold"),
             fg=TEXT_MUTED, bg=BG_PANEL).pack(side="left")
    tk.Frame(f, bg=BORDER, height=1).pack(side="left", fill="x", expand=True, padx=(8, 0), pady=6)
    return f


def styled_entry(parent, textvariable, width=30):
    e = tk.Entry(parent, textvariable=textvariable, width=width,
                 bg=BG_INPUT, fg=TEXT, insertbackground=TEXT,
                 relief="flat", font=("Consolas", 10),
                 highlightthickness=1, highlightbackground=BORDER,
                 highlightcolor=ACCENT)
    return e


def styled_combo(parent, values, textvariable, width=28):
    style = ttk.Style()
    style.theme_use("clam")
    style.configure("Dark.TCombobox",
                     fieldbackground=BG_INPUT, background=BG_INPUT,
                     foreground=TEXT, arrowcolor=TEXT_DIM,
                     bordercolor=BORDER, lightcolor=BORDER, darkcolor=BORDER,
                     selectbackground=BG_INPUT, selectforeground=TEXT)
    style.map("Dark.TCombobox",
              fieldbackground=[("readonly", BG_INPUT)],
              foreground=[("readonly", TEXT)],
              background=[("active", BG_HOVER)])
    c = ttk.Combobox(parent, values=values, textvariable=textvariable,
                     width=width, style="Dark.TCombobox",
                     state="readonly", font=("Consolas", 10))
    return c


