    def _scan(self):
        if not self.app.root_folders:
            self.scan_status.configure(text="Add a root folder first.")
            return
        self.scan_status.configure(text="Scanning…")
        self.update_idletasks()
        t = threading.Thread(target=self._scan_worker, daemon=True)
        t.start()

    def _scan_worker(self):
        lang = self.scan_lang_var.get().strip() or "no"
        entries = []
        for root in self.app.root_folders:
            root_path = Path(root)
            if not root_path.exists():
                continue
            self.after(0, lambda r=root: self.scan_status.configure(
                text=f"Scanning {Path(r).name}…"))
            subfolders = sorted(
                [root_path] + [d for d in root_path.rglob("*") if d.is_dir()],
                key=lambda p: str(p).lower())
            processed_dirs = set()
            for d in subfolders:
                if d in processed_dirs:
                    continue
                try:
                    videos = [f for f in d.iterdir()
                              if f.is_file() and f.suffix.lower() in VIDEO_EXTENSIONS]
                except PermissionError:
                    continue
                if not videos:
                    continue
                if any(str(d).startswith(str(pd) + os.sep) for pd in processed_dirs):
                    continue
                all_vids = sorted([f for f in d.rglob("*")
                                   if f.is_file() and f.suffix.lower() in VIDEO_EXTENSIONS])
                ep_statuses = []
                episodes = []
                for v in all_vids:
                    cached = self.app.library_db.get(v)
                    status, our, srt, fp = scan_video_status(v, lang, cached)
                    self.app.library_db.update(v, status, our, srt, fp)
                    ep_statuses.append(status)
                    episodes.append({"path": str(v), "status": status,
                                     "our": our, "srt": srt})

                if not ep_statuses:
                    continue

                status_set = set(ep_statuses)
                if status_set == {"DONE"}:
                    agg = "DONE"
                elif status_set == {"NEW"}:
                    agg = "NEW"
                elif all(s in ("NEW", "UNPROCESSED") for s in ep_statuses):
                    agg = "UNPROCESSED"
                elif "CHANGED" in status_set:
                    agg = "CHANGED"
                elif "NEW" in status_set or "UNPROCESSED" in status_set:
                    agg = "PARTIAL"
                elif "EXTERNAL" in status_set:
                    agg = "EXTERNAL"
                else:
                    agg = "DONE"

                entries.append({
                    "path":     str(d),
                    "status":   agg,
                    "total":    len(all_vids),
                    "done":     sum(1 for s in ep_statuses if s == "DONE"),
                    "root":     root,
                    "episodes": episodes,
                })
                processed_dirs.add(d)
                self.after(0, lambda n=len(entries): self.scan_status.configure(
                    text=f"Scanning… {n} folders found"))

        self.lib_entries = entries
        self.app.lib_entries = entries
        self.app.library_db.data["__entries__"] = entries
        self.app.library_db.save()
        self.after(0, self._populate)
        self.after(0, lambda: self.scan_status.configure(
            text=f"Done — {len(entries)} folders indexed."))

    def _populate(self):
        self.tree.delete(*self.tree.get_children())
        filt   = self.filter_var.get()
        search = self.search_var.get().lower().strip()
        shown  = 0

        for e in self.lib_entries:
            # Filter by status
            if filt != "ALL" and e["status"] != filt:
                continue
            # Filter by search
            name = Path(e["path"]).name
            if search and search not in name.lower() and search not in e["path"].lower():
                continue

            # Relative display name
            try:
                rel = str(Path(e["path"]).relative_to(e["root"]))
                if not rel or rel == ".":
                    rel = Path(e["root"]).name
            except Exception:
                rel = name

            badge = {"NEW":         "● NEW",
                     "UNPROCESSED":  "○ UNPROCESSED",
                     "DONE":         "✓ DONE",
                     "PARTIAL":      "◑ PARTIAL",
                     "CHANGED":      "▲ CHANGED",
                     "EXTERNAL":     "~ EXTERNAL"}.get(e["status"], e["status"])

            parent = self.tree.insert("", "end", text=rel,
                                       values=(badge, e["total"], e["done"], e["path"]),
                                       tags=(e["status"],), open=False)

            # Insert episode children
            for ep in e.get("episodes", []):
                ep_name = Path(ep["path"]).name
                if search and search not in ep_name.lower():
                    continue
                ep_badge = {"NEW":        "● NEW",
                            "UNPROCESSED":"○ UNPROCESSED",
                            "DONE":       "✓ DONE",
                            "CHANGED":    "▲ CHANGED",
                            "EXTERNAL":   "~ EXTERNAL"}.get(ep["status"], ep["status"])
                srt_name = Path(ep["srt"]).name if ep.get("srt") else ""
                detail = f"  {ep_name}"
                if srt_name:
                    detail += f"   [{srt_name}]"
                self.tree.insert(parent, "end",
                                  text=detail,
                                  values=(ep_badge, "", "", ep["path"]),
                                  tags=(ep["status"],))
            shown += 1

        self.count_label.configure(
            text=f"{shown} / {len(self.lib_entries)} folders shown")

    def _sel_all(self):
        # Select only top-level items
        self.tree.selection_set(self.tree.get_children())

    def _sel_status(self, status):
        self.tree.selection_remove(self.tree.get_children(""))
        for iid in self.tree.get_children(""):
            if status in self.tree.item(iid)["tags"]:
                self.tree.selection_add(iid)

    def _expand_all(self):
        for iid in self.tree.get_children():
            self.tree.item(iid, open=True)

    def _collapse_all(self):
        for iid in self.tree.get_children():
            self.tree.item(iid, open=False)

    def _process(self):
        sel = self.tree.selection()
        if not sel:
            self.scan_status.configure(text="Select folders or episodes first.")
            return
        lang = self.scan_lang_var.get().strip() or "no"
        skip_done = self.app.skip_no_var.get()
        video_files = []

        for iid in sel:
            parent = self.tree.parent(iid)
            if parent:
                # It's an episode — add directly
                path_val = self.tree.item(iid)["values"][3]
                # Strip srt_info suffix if present
                path_str = path_val.split("  ")[0].strip()
                p = Path(path_str)
                if p.exists() and p.suffix.lower() in VIDEO_EXTENSIONS:
                    video_files.append(p)
            else:
                # It's a folder — walk it
                folder = Path(self.tree.item(iid)["values"][3])
                for root, dirs, files in os.walk(folder):
                    dirs.sort(key=lambda d: (
                        int(''.join(filter(str.isdigit, d)) or 0), d.lower()))
                    for f in sorted(files):
                        p = Path(root) / f
                        if p.suffix.lower() not in VIDEO_EXTENSIONS:
                            continue
                        if skip_done:
                            cached = self.app.library_db.get(p)
                            status, _, _, _ = scan_video_status(p, lang, cached)
                            if status == "DONE":
                                continue
                        video_files.append(p)

        # Deduplicate preserving order
        seen = set()
        unique = []
        for v in video_files:
            if str(v) not in seen:
                seen.add(str(v))
                unique.append(v)

        if not unique:
            self.scan_status.configure(text="No files to process (all DONE?).")
            return

        self.scan_status.configure(text=f"Sending {len(unique)} file(s) to processor…")
        self.app._queue_from_library(unique)
        self.destroy()


