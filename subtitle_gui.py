    # ── Job list helpers ──────────────────────────────────────────────────────
    def _add_jobs(self, paths: list):
        """Add a list of Path objects to the job list, skipping duplicates."""
        existing = {j["path"] for j in self.job_list}
        added = 0
        for p in paths:
            if p in existing:
                continue
            iid = self.job_tree.insert("", "end",
                                        values=("○", p.name),
                                        tags=("pending",))
            self.job_list.append({"path": p, "status": "pending", "iid": iid})
            existing.add(p)
            added += 1
        self._update_job_label()
        if added:
            # Scroll to first pending item
            for j in self.job_list:
                if j["status"] == "pending":
                    self.job_tree.see(j["iid"])
                    break

    def _set_job_status(self, iid: str, status: str):
        icons = {"pending": "○", "running": "▶", "done": "✓", "error": "✗"}
        self.job_tree.item(iid, values=(icons.get(status, "?"),
                                        self.job_tree.item(iid)["values"][1]),
                            tags=(status,))
        self.job_tree.see(iid)

    def _job_remove(self):
        sel = self.job_tree.selection()
        for iid in sel:
            # Don't remove currently running item
            job = next((j for j in self.job_list if j["iid"] == iid), None)
            if job and job["status"] == "running":
                continue
            self.job_tree.delete(iid)
            self.job_list = [j for j in self.job_list if j["iid"] != iid]
        self._update_job_label()

    def _job_clear_done(self):
        for j in list(self.job_list):
            if j["status"] in ("done", "error"):
                self.job_tree.delete(j["iid"])
                self.job_list.remove(j)
        self._update_job_label()

    def _job_clear_all(self):
        for j in list(self.job_list):
            if j["status"] != "running":
                self.job_tree.delete(j["iid"])
                self.job_list.remove(j)
        self._update_job_label()

    def _update_job_label(self):
        total   = len(self.job_list)
        pending = sum(1 for j in self.job_list if j["status"] == "pending")
        done    = sum(1 for j in self.job_list if j["status"] == "done")
        errors  = sum(1 for j in self.job_list if j["status"] == "error")
        if total == 0:
            self.file_label.configure(text="Queue is empty.", fg=TEXT_MUTED)
        else:
            parts = [f"{pending} pending", f"{done} done"]
            if errors:
                parts.append(f"{errors} failed")
            self.file_label.configure(text="  |  ".join(parts), fg=TEXT_DIM)

