    def _lib_load_from_cache(self):
        """Load library entries from the JSON cache without touching the NAS."""
        import json, time
        self.lib_entries = []
        cached = self.library_db.data.get("__entries__", [])
        for e in cached:
            if any(e.get("root") == r or e.get("path", "").startswith(r) 
                   for r in self.root_folders):
                self.lib_entries.append(e)
        self._lib_apply_filter()

    def _lib_scan(self):
        """Trigger a background scan — called from LibraryWindow or manually."""
        if not self.root_folders:
            return
        t = threading.Thread(target=self._lib_scan_worker, daemon=True)
        t.start()

    def _lib_scan_worker(self):
        """Background scan worker — updates library_db, no UI references."""
        lang = self.scan_lang_var.get().strip() or "no"
        entries = []
        for root in self.root_folders:
            root_path = Path(root)
            if not root_path.exists():
                continue
            subfolders = sorted(
                [root_path] + [d for d in root_path.rglob("*") if d.is_dir()],
                key=lambda p: str(p).lower())
            processed_dirs = set()
            for d in subfolders:
                if d in processed_dirs:
                    continue
                try:
                    videos = [f for f in d.iterdir()
                              if f.is_file() and f.suffix.lower() in VIDEO_EXTENSIONS]
                except PermissionError:
                    continue
                if not videos:
                    continue
                if any(str(d).startswith(str(pd) + os.sep) for pd in processed_dirs):
                    continue
                all_vids = [f for f in d.rglob("*")
                            if f.is_file() and f.suffix.lower() in VIDEO_EXTENSIONS]
                statuses = []
                for v in all_vids:
                    cached = self.library_db.get(v)
                    status, our, srt, fp = scan_video_status(v, lang, cached)
                    self.library_db.update(v, status, our, srt, fp)
                    statuses.append(status)
                if not statuses:
                    continue
                status_set = set(statuses)
                if status_set == {"DONE"}:                              agg = "DONE"
                elif status_set == {"NEW"}:                             agg = "NEW"
                elif all(s in ("NEW","UNPROCESSED") for s in statuses): agg = "UNPROCESSED"
                elif "CHANGED" in status_set:                           agg = "CHANGED"
                elif "NEW" in status_set or "UNPROCESSED" in status_set: agg = "PARTIAL"
                elif "EXTERNAL" in status_set:                          agg = "EXTERNAL"
                else:                                                   agg = "DONE"
                entries.append({
                    "path":   str(d), "status": agg,
                    "total":  len(all_vids),
                    "done":   sum(1 for s in statuses if s == "DONE"),
                    "root":   root,
                })
                processed_dirs.add(d)
        self.lib_entries = entries
        self.library_db.data["__entries__"] = entries
        self.library_db.save()

